.MODEL SMALL
.STACK
.DATA

NO DW 0FFFFH
M1 DB 10,13,"HEX TO BCD: $";
COUNT DB 0

.CODE

DISP MACRO XX
MOV AH,09
LEA DX,XX
INT 21H
ENDM

.STARTUP

DISP M1

MOV AX,NO
MOV BX,0AH
MOV DX,0

BACK:
MOV DX,0
DIV BX
PUSH DX

INC COUNT
CMP AX,0000H
JNZ BACK

BACK1:
POP DX
ADD DL,30H
MOV AH,02
INT 21H
DEC COUNT
JNZ BACK1
.EXIT
END